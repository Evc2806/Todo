
# Creamos una lista de números
numeros = [1, 2, 3, 4, 5]

# Imprimimos la lista
print("Lista original:", numeros)

# Accedemos al primer elemento de la lista (índice 0)
print("Primer elemento:", numeros[0])

# Accedemos al último elemento de la lista
print("Último elemento:", numeros[-1])

# Modificamos un elemento de la lista
numeros[2] = 10

# Añadimos un nuevo elemento al final de la lista
numeros.append(6)

# Eliminamos el segundo elemento de la lista
del numeros[1]

# Imprimimos la lista actualizada
print("Lista actualizada:", numeros)

# Imprimimos la longitud de la lista
print("Longitud de la lista:", len(numeros))