import numpy as np

# Crear un numpy array a partir de una lista
array = np.array([1, 2, 3, 4, 5])
print("Arreglo:", array)

# Realizar operaciones matemáticas
suma = np.sum(array)
print("Suma de todos los elementos:", suma)

media = np.mean(array)
print("Media de los elementos:", media)

maximo = np.max(array)
print("Valor máximo:", maximo)

minimo = np.min(array)
print("Valor mínimo:", minimo)